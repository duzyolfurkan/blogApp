////////////////////////////////////////////////////////////// Kullanılan paketlerin ve dosyaların entegrasyonu //////////////////////////////////////////////////////////////////////
const express = require("express");
const app = express();
const path = require("path");
const sequelize = require("./data/db");
const dummyData = require("./data/dummy-data");
const Blog = require("./models/blog");
const Category = require("./models/category");
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////// EJS Middleware kullanıma açılması //////////////////////////////////////////////////////////////////////////////////
app.set("view engine", "ejs");
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////// Express'in post metodlarındaki veriyi encode eden metodu kullanıma açıldı ////////////////////////////////////////////////////////
app.use(express.urlencoded({ extended: false }));
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////// Dışarıdan eklenen dosyaların tanıtılması ve entegre edilip yollarının verilmesi /////////////////////////////////////////////////////////////////
app.use("/libs", express.static(path.join(__dirname, "node_modules")));
app.use("/static", express.static(path.join(__dirname, "public")));
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////// Route dosyalarının tanıtılması ve yollarının verilmesi /////////////////////////////////////////////////////////////////
const userRoutes = require("./routes/user");
const adminRoutes = require("./routes/admin");
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////// Tanımlanan route dosyaları çalıştırıldı ///////////////////////////////////////////////////////////////////////////
app.use("/admin", adminRoutes);
app.use(userRoutes);
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////// İlişklilerin tanımlanması /////////////////////////////////////////////////////////////////////////////////////////
// //BİREÇOK BAĞLANTI
