///////////////////////////////////////////Kütüphane entegre edildi///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const slugify = require('slugify');
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////Kütüphane özellikleri tanımlandı ////////////////////////////////////////////////////////////////////////////////////////////////
const options = {
    replacement: '-',
    remove: undefined,
    lower: true,
    strict: true,
    locale: 'tr',
    trim: true
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////Kütüphane dışarıya açıldı/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
module.exports = function slugField(str) {
    return slugify(str, options);
}
